int	ft_atoi(const char *str)
{
	int	num;
	int	sign;

	while (*str && (*str == ' ' || (*str >= 9 && *str <= 13)))
		str++;
	num = 0;
	sign = 1;
	if (*str && (*str == '+' || *str == '-'))
	{
		if (*str == '-')
			sign = -sign;
		str++;
	}
	while (*str && *str >= '0' && *str <= '9')
	{
		num = num * 10 + *str - '0';
		str++;
	}
	return (sign * num);
}
